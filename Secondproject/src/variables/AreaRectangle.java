package variables;

public class AreaRectangle {
	public static void main(String[] args) {
		double length;
		double breadth;
		double area;
		//Assign length and breadth;
		length = 20;
		breadth = 16;
		//Assign formula
		area = length * breadth;
		
		// display results
		System.out.println("The area of the rectangle is: " + area);
		
	}

}
